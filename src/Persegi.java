public class Persegi {

    public int persegi(int s){
        System.out.println("Menghitung luas persegi");
        System.out.println("Rumus: S * S");
        return (s * s);
    }

    public double persegi(double s){
        System.out.println("Menghitung keliling persegi");
        System.out.println("Rumus: S + S + S + S");
        return (s + s + s + s);
    }
}
