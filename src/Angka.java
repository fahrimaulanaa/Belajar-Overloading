public class Angka {
    public int persegipanjang(int p, int l){
        System.out.println("Luas persegi panjang");
        System.out.println("Rumus: p x l");
        return (p * l);
    }

    public double persegipanjang(double p, double l ){
        System.out.println("Keliling persegi panjang");
        System.out.println("Rumus: (2 x p) + (2 x l)");
        return ((2 * p)+ (2 * l));
    }
}
